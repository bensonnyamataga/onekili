            <h3>Optimal Climbing Seasons</h3>
            <p>The two dry seasons provide the best climbing conditions:</p>
            
            <p><strong>June to October (Long Dry Season):</strong> Excellent conditions with clear skies, minimal precipitation, and comfortable temperatures. This is the most popular time with the highest success rates.</p>
            
            <p><strong>December to March (Short Dry Season):</strong> Warmer temperatures with generally good conditions. The northern location means even better dry conditions than southern routes during this period.</p>

            <h3>Shoulder Seasons</h3>
            <p>Transition periods with more variable conditions:</p>
            
            <p><strong>January-February:</strong> Warm and dry with excellent visibility and good climbing conditions</p>
            
            <p><strong>November:</strong> "Short rains" period but the northern location often remains drier than southern routes</p>

            <h3>Rainy Seasons</h3>
            <p>More challenging but still possible with proper preparation:</p>
            
            <p><strong>March to May (Long Rains):</strong> Heavier rainfall but the northern location typically experiences less rain than southern routes. Still requires comprehensive wet weather gear.</p>
            
            <p><strong>Considerations:</strong> The camping accommodation means you need high-quality waterproof gear during rainy periods.</p>

