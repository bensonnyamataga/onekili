            <h2>Accommodation on the Rongai Route</h2>
            <p>The Rongai Route features camping accommodation throughout, providing a true wilderness experience under the African skies.</p>

            <h3>Camping Experience Overview</h3>
            <p>Full-service camping with professional support:</p>
            
            <p><strong>Sleeping Arrangements:</strong> High-quality mountain tents typically for 2 people</p>
            
            <p><strong>Dining Facilities:</strong> Spacious dining tent with tables and chairs for meals</p>
            
            <p><strong>Water Sources:</strong> Treated water provided by the crew throughout the climb</p>
            
            <p><strong>Bathroom Facilities:</strong> Portable toilet tents provided at each campsite</p>

            <h3>Campsite Details</h3>
            <p>Each camp offers unique locations and experiences:</p>
            
            <p><strong>First Cave Camp (2,600m):</strong> Forest campsite with basic facilities</p>
            
            <p><strong>Kikelewa Camp (3,600m):</strong> Moorland location with stunning views of Mawenzi peak</p>
            
            <p><strong>Mawenzi Tarn Camp (4,330m):</strong> Spectacular high-altitude camp beneath Mawenzi's cliffs</p>
            
            <p><strong>Kibo Hut (4,703m):</strong> Base camp for summit attempt, shared with Marangu Route climbers</p>

            <h3>What to Expect</h3>
            <p>Realistic expectations for the camping experience:</p>
            
            <p><strong>Comfort Level:</strong> Well-organized with comfortable sleeping and dining arrangements</p>
            
            <p><strong>Temperature:</strong> Cold nights requiring high-quality sleeping bags and insulation</p>
            
            <p><strong>Privacy:</strong> Good privacy in personal tents with communal dining areas</p>
            
            <p><strong>Atmosphere:</strong> Wilderness camping experience with small group camaraderie</p>

