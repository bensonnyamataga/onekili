            <h2>The Mental Battle: 2:00 AM to 4:00 AM</h2>
            <p>Between approximately 2:00 AM and 4:00 AM, most climbers encounter the deepest mental challenge of the entire expedition. The initial excitement has dissipated, replaced by mounting fatigue. The summit remains hours away, invisible in the darkness above. This is when internal dialogues become critical. Some climbers bargain with themselves: "Just to the next rock formation." Others repeat personal mantras or focus entirely on their breathing rhythm. Many experience moments of profound doubt, questioning their decision to attempt the climb at all.</p>

            <p>The guides' role becomes particularly crucial during these hours. They constantly monitor each climber's condition, offering encouragement, sometimes physically assisting by carrying a daypack for a struggling climber, or providing an arm for support on particularly steep sections. They share stories of previous successful summits, point out progress that's invisible to climbers focused only on their immediate footsteps, and remind everyone that the hardest part is temporary. The cultural differences in guiding approaches become apparent here—some guides sing traditional Chagga songs, others offer quiet one-on-one encouragement, all with the same goal of keeping climbers moving upward.</p>

            <p>Altitude effects intensify during this period. Headaches that were mild at camp may become more pronounced. Nausea affects some climbers, making hydration and nutrition challenging despite their necessity. The cold becomes more than discomfort—it becomes a physiological stressor that saps energy reserves. Proper preparation shows its value during these hours: climbers with well-tested layering systems, adequate hydration strategies, and appropriate pacing fare significantly better than those who underestimated any of these elements.</p>

