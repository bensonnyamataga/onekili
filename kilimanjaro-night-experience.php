            <h2>Midnight Departure: Stepping into the Unknown</h2>
            <p>When you step out of your tent around midnight, the experience immediately feels different from any previous day of trekking. The cold is more intense, penetrating through layers despite careful preparation. The world has reduced to the circle of light from your headlamp, creating a tunnel vision effect where you can only see the boots of the person ahead and the immediate ground before you. The line of headlamps winding up the mountain creates one of Kilimanjaro's most iconic sights—a serpentine trail of human determination against the vast African night.</p>

            <p>The first hour typically feels manageable, even energizing. The excitement carries you upward, and the steepness hasn't yet reached its peak. Breathing remains relatively normal for the altitude, and the rhythm of "pole pole" (slowly slowly) feels comfortable. This initial period is deceptive, and experienced guides use it to establish a sustainable pace that will conserve energy for the more challenging hours ahead. During this phase, climbers often experience a sense of camaraderie, with whispered encouragement exchanged in the darkness.</p>

            <p>As you progress beyond the first hour, several physiological and psychological realities converge. The air grows noticeably thinner with each altitude gain. Breathing becomes a conscious activity—deliberate inhalations that never seem to provide quite enough oxygen. The steepness of the trail increases progressively, with some sections requiring using trekking poles for additional support and balance. The cold, which was initially just uncomfortable, begins to seep through layers, particularly affecting extremities despite glove liners, insulated gloves, and hand warmers.</p>

