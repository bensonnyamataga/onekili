            <h2>The Final Preparation: Hours Before Midnight Ascent</h2>
            <p>The summit night experience truly begins during the late afternoon at high camp. After arriving at Barafu Camp (4,673m) or Kosovo Camp (4,800m) depending on your route, there's a palpable shift in atmosphere. The usual camp chatter becomes more subdued, replaced by focused preparation and quiet contemplation. The guides conduct a thorough summit briefing, reviewing the timeline, expected conditions, and emergency procedures. This briefing covers everything from layering systems to recognizing altitude sickness symptoms that would necessitate turning back.</p>

            <p>Dinner is served early, typically around 5:00 PM, with emphasis on carbohydrate-rich foods that will provide sustained energy through the long night ahead. Most climbers experience diminished appetite at this altitude, but eating adequately is crucial. Following dinner, the final gear check occurs. Every item is inspected: headlamps with fresh batteries, insulated water systems to prevent freezing, properly layered clothing systems, and emergency supplies. The guides emphasize the importance of nothing new—every piece of gear should have been tested during previous days of the trek.</p>

            <p>The attempt to sleep between 7:00 PM and 11:00 PM is often more symbolic than restful. At this altitude, with anticipation running high, sleep comes in brief fragments at best. Many climbers report simply lying in their sleeping bags, listening to the wind against the tent, mentally rehearsing the hours ahead. The guides wake the team around 11:00 PM for final preparations—forcing down some tea and biscuits, completing final clothing layers, and stepping out into what feels like another world entirely.</p>

