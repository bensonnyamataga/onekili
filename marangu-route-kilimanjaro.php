            <h2>Accommodation on the Marangu Route</h2>
            <p>The hut system is Marangu's most distinctive feature, offering a different experience from the camping routes.</p>

            <h3>Mountain Huts Overview</h3>
            <p>All huts provide basic but adequate facilities:</p>
            
            <p><strong>Sleeping Arrangements:</strong> Dormitory-style bunk beds with mattresses (sleeping bag still required)</p>
            
            <p><strong>Dining Facilities:</strong> Separate dining huts where meals are served</p>
            
            <p><strong>Water Sources:</strong> Treated water available at all camps</p>
            
            <p><strong>Bathroom Facilities:</strong> Basic toilet blocks with western-style toilets and limited washing facilities</p>

            <h3>Hut-Specific Details</h3>
            <p>Each camp offers different experiences and facilities:</p>
            
            <p><strong>Mandara Huts:</strong> Located in beautiful rainforest setting with smaller, more intimate huts</p>
            
            <p><strong>Horombo Huts:</strong> Largest camp with capacity for 120 people, stunning views of Kibo peak</p>
            
            <p><strong>Kibo Huts:</strong> Most basic facilities at highest elevation, serving as launch point for summit attempt</p>

            <h3>What to Expect</h3>
            <p>Realistic expectations for hut accommodation:</p>
            
            <p><strong>Comfort Level:</strong> Basic but adequate - not luxury accommodation</p>
            
            <p><strong>Temperature:</strong> Unheated huts can be very cold at higher elevations</p>
            
            <p><strong>Privacy:</strong> Limited - shared dormitory-style accommodation</p>
            
            <p><strong>Atmosphere:</strong> Social and communal with opportunity to meet other international climbers</p>

            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-bed"></i>
                </div>
                <p>While the huts provide shelter from wind and rain, they can be quite cold, especially at Horombo and Kibo Huts. A high-quality sleeping bag rated to at least -10°C (14°F) is essential, along with warm sleeping clothes. The communal nature of the huts also means earplugs can be valuable for light sleepers.</p>
            </div>

