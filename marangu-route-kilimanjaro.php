            <h2>Difficulty Level and Physical Requirements</h2>
            <p>While often marketed as the "easiest" Kilimanjaro route, Marangu still presents significant challenges that should not be underestimated.</p>

            <h3>Technical Difficulty</h3>
            <p>From a technical perspective, Marangu is considered the least challenging route:</p>
            
            <p><strong>Trail Conditions:</strong> Well-defined, maintained paths throughout with no technical climbing sections</p>
            
            <p><strong>Summit Night:</strong> Steep but non-technical scree slope ascent to Gilman's Point</p>
            
            <p><strong>Descent:</strong> Retraces the same path, which can be hard on knees but is generally manageable</p>

            <h3>Physical Demands</h3>
            <p>The physical challenge remains substantial:</p>
            
            <p><strong>Summit Day/Night:</strong> 11-15 hours of continuous hiking with 1,200m ascent and descent</p>
            
            <p><strong>Altitude:</strong> Rapid gain, especially on the 5-day itinerary, increases altitude sickness risk</p>
            
            <p><strong>Endurance:</strong> Multiple consecutive days of hiking with significant elevation changes</p>

            <div class="highlight-box secondary">
                <div class="highlight-icon">
                    <i class="fas fa-hiking"></i>
                </div>
                <p><strong>Guide Insight:</strong> Many climbers mistakenly believe Marangu is "easy" because of the huts and gradual slope. In reality, the rapid altitude gain, especially on the 5-day itinerary, makes altitude sickness a significant concern. We strongly recommend the 6-day option for better acclimatization and higher summit success.</p>
            </div>

            <h2>Best Time to Climb the Marangu Route</h2>
            <p>The Marangu Route can be climbed year-round, but certain seasons offer significantly better conditions for summit success and overall experience.</p>

            <div class="image-with-caption">
                <img src="img/dry-season-kilimanjaro.jpg" alt="Best climbing seasons for Marangu Route">
                <p>The dry seasons offer the best conditions for Marangu Route climbs</p>
            </div>

            <h3>Optimal Climbing Seasons</h3>
            <p>The two dry seasons provide the best climbing conditions:</p>
            
            <p><strong>June to October (Long Dry Season):</strong> Clear skies, minimal precipitation, and excellent trail conditions. This is the most popular time with the highest success rates.</p>
            
            <p><strong>December to March (Short Dry Season):</strong> Warmer temperatures with generally good conditions. Slightly higher chance of precipitation but still excellent for climbing.</p>

            <h3>Shoulder Seasons</h3>
            <p>Transition periods with more variable conditions:</p>
            
            <p><strong>January-February:</strong> Warm and generally dry with good visibility</p>
            
            <p><strong>November:</strong> "Short rains" period with potential afternoon showers but beautiful green landscapes</p>

            <h3>Rainy Seasons</h3>
            <p>Challenging but possible with proper preparation:</p>
            
            <p><strong>March to May (Long Rains):</strong> Heavy rainfall, muddy trails, and limited visibility. Only recommended for experienced climbers with proper wet weather gear.</p>
            
            <p><strong>Considerations:</strong> The hut accommodation provides shelter during rainy periods, making Marangu more manageable than camping routes during wet weather.</p>

            <div class="stat-box-container">
                <div class="stat-box">
