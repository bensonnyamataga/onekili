                                </div>
                                <div class="safari-faq-answer">
                                    <p>The Umbwe Route requires substantial mountain experience. You should have previous high-altitude experience (preferably above 5,000m), excellent physical fitness, and experience with multi-day trekking in challenging conditions. This route is not suitable for first-time high-altitude climbers. We typically require climbers to have summited other major peaks or have extensive mountain hiking experience before attempting Umbwe.</p>
                                </div>
                            </div>

                            <div class="safari-faq-item">
                                <div class="safari-faq-question">
                                    <h4>Why is the success rate so low on the Umbwe Route?</h4>
                                    <span class="safari-faq-toggle">+</span>
                                </div>
                                <div class="safari-faq-answer">
                                    <p>The Umbwe Route's low success rate (60-65% on the 7-day itinerary) results from three main factors: the rapid altitude gain that doesn't allow proper acclimatization, the extreme physical demands that exhaust climbers before summit day, and the fact that it attracts experienced climbers who may overestimate their abilities. Even with the 7-day itinerary, the acclimatization profile is poor compared to routes like Lemosho or Northern Circuit.</p>
                                </div>
                            </div>

                            <div class="safari-faq-item">
                                <div class="safari-faq-question">
                                    <h4>Can first-time Kilimanjaro climbers attempt the Umbwe Route?</h4>
                                    <span class="safari-faq-toggle">+</span>
                                </div>
                                <div class="safari-faq-answer">
                                    <p>We strongly discourage first-time Kilimanjaro climbers from attempting the Umbwe Route. The combination of extreme physical difficulty and poor acclimatization makes it unsuitable for beginners. First-time climbers should choose routes with better acclimatization profiles like Lemosho (8-day) or Northern Circuit. Umbwe should only be considered by climbers with proven high-altitude experience and exceptional fitness levels.</p>
                                </div>
                            </div>

                            <div class="safari-faq-item">
                                <div class="safari-faq-question">
