            <h2>Accommodation on the Umbwe Route</h2>
            <p>The Umbwe Route offers a true wilderness camping experience with basic facilities reflecting its remote character.</p>

            <h3>Camping Experience Overview</h3>
            <p>Minimalist camping in remote locations:</p>
            
            <p><strong>Sleeping Arrangements:</strong> Basic mountain tents in remote, often exposed campsites</p>
            
            <p><strong>Dining Facilities:</strong> Simple dining tent setup due to limited space at camps</p>
            
            <p><strong>Camp Services:</strong> Basic toilet facilities, limited water sources at higher camps</p>
            
            <p><strong>Support Team:</strong> Smaller, highly experienced crew for this specialized route</p>

            <h3>Campsite Characteristics</h3>
            <p>Remote and often challenging campsite locations:</p>
            
            <p><strong>Umbwe Cave Camp:</strong> Unique campsite at a natural cave formation</p>
            
            <p><strong>Barranco Camp:</strong> Joins main routes at this dramatic campsite beneath Barranco Wall</p>
            
            <p><strong>Exposed Locations:</strong> Higher camps often in more exposed, windier locations</p>
            
            <p><strong>Limited Space:</strong> Small, basic campsites due to steep terrain</p>

            <h3>What to Expect</h3>
            <p>Rugged wilderness experience:</p>
            
            <p><strong>Comfort Level:</strong> Basic - this is an expedition-style experience</p>
            
            <p><strong>Temperature:</strong> Colder and windier than more protected routes</p>
            
            <p><strong>Privacy:</strong> Complete wilderness solitude</p>
            
            <p><strong>Atmosphere:</strong> Expedition feeling with small, focused team</p>

            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-campground"></i>
                </div>
                <p>The camping experience on Umbwe is more expedition-style than other routes. Campsites are smaller, more basic, and often in exposed locations. You'll need a high-quality four-season sleeping bag rated to -20°C (-4°F) and excellent cold-weather gear. The remote nature means there are fewer facilities and services compared to main routes. This is part of the appeal for experienced mountaineers seeking a pure wilderness experience.</p>
