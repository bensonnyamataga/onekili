                                    <span class="safari-faq-toggle">+</span>
                                </div>
                                <div class="safari-faq-answer">
                                    <p>The 7-day itinerary includes an extra acclimatization day at Barranco Camp (3,900m) with a hike to higher elevation before descending to sleep. This "climb high, sleep low" strategy is crucial for Umbwe given its rapid ascent profile. The 7-day option increases success rates from approximately 55% to 65% and significantly improves safety. The 6-day itinerary should only be considered by climbers with exceptional altitude tolerance and recent high-altitude experience.</p>
                                </div>
                            </div>

