            <h2>Accommodation on the Lemosho Route</h2>
            <p>The Lemosho Route is a camping-only experience, offering a true wilderness adventure with comfortable tent accommodations.</p>

            <h3>Camping Experience Overview</h3>
            <p>Full-service camping with professional support:</p>
            
            <p><strong>Sleeping Arrangements:</strong> High-quality mountain tents (typically 2-person sharing)</p>
            
            <p><strong>Dining Facilities:</strong> Spacious dining tent with tables, chairs, and full meal service</p>
            
            <p><strong>Camp Services:</strong> Portable toilet tents provided at each campsite</p>
            
            <p><strong>Support Team:</strong> Dedicated crew handles all camp setup, cooking, and logistics</p>

            <h3>Campsite Highlights</h3>
            <p>Each camp offers unique settings and experiences:</p>
            
            <p><strong>Forest Camps:</strong> Lush rainforest settings with sounds of wildlife</p>
            
            <p><strong>Shira Camp 1 & 2:</strong> High-altitude plateau with spectacular views of Kibo peak</p>
            
            <p><strong>Barranco Camp:</strong> Dramatic location at the base of the famous Barranco Wall</p>
            
            <p><strong>Karanga Camp:</strong> Last water source before summit with stunning valley views</p>

            <h3>What to Expect</h3>
            <p>Realistic expectations for the camping experience:</p>
            
            <p><strong>Comfort Level:</strong> Surprisingly comfortable with proper equipment and support</p>
            
            <p><strong>Temperature:</strong> Cold nights at higher elevations require warm sleeping bags</p>
            
            <p><strong>Privacy:</strong> More privacy than hut accommodation with personal tent space</p>
            
            <p><strong>Atmosphere:</strong> Wilderness immersion with small group camaraderie</p>

            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-campground"></i>
                </div>
                <p>The camping experience on Lemosho is fully supported, meaning you only carry a daypack while hiking. Our crew handles all camp setup, cooking, and logistics. While it lacks the solid walls of Marangu's huts, many climbers find they sleep better in tents due to better ventilation and personal space. A high-quality sleeping bag rated to -15°C (5°F) is essential for comfort at higher camps.</p>
            </div>

