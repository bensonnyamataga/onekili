            <h2>Trip Cancellation and Interruption Protection</h2>
            <p>Kilimanjaro expeditions represent significant financial investments that should be protected against unforeseen circumstances.</p>

            <h3>Cancellation Coverage</h3>
            <p>Protect your investment if you cannot climb due to covered reasons:</p>
            
            <p><strong>Medical Cancellation:</strong> Coverage if you or a family member becomes ill or injured before your trip, preventing you from climbing.</p>
            
            <p><strong>Job Loss Protection:</strong> Some policies cover cancellation if you lose your job within a specified period before your climb.</p>
            
            <p><strong>Natural Disasters:</strong> Coverage if volcanic activity, earthquakes, or other natural disasters prevent access to the mountain.</p>

            <div class="highlight-box secondary">
                <div class="highlight-icon">
