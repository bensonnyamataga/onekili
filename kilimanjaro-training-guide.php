            <h2>Developing Mountain-Ready Strength</h2>
            <p>While cardiovascular fitness takes priority, strength training plays a crucial supporting role in injury prevention and climbing efficiency. Focus on functional strength that directly translates to mountain movement.</p>

            <h3>Essential Strength Exercises</h3>
            <p>Incorporate these key movements into your strength training routine 2-3 times per week:</p>
            
            <p><strong>Lower Body Focus:</strong> Your legs will be doing most of the work on Kilimanjaro. Squats, lunges, step-ups, and calf raises build the quadriceps, glutes, hamstrings, and calves needed for sustained climbing.</p>
            
            <p><strong>Core Stability:</strong> A strong core improves balance on uneven terrain and reduces back strain from carrying a pack. Planks, Russian twists, and bird-dogs should be staples of your routine.</p>
            
            <p><strong>Upper Body Support:</strong> While less critical than lower body strength, your shoulders and back help stabilize your pack. Rows, shoulder presses, and pull-ups provide balanced upper body development.</p>

            <div class="highlight-box secondary">
                <div class="highlight-icon">
                    <i class="fas fa-dumbbell"></i>
                </div>
                <p><strong>Training Insight:</strong> Many climbers overlook downhill training, but eccentric muscle contractions during descent cause significant muscle soreness. Include downhill hiking in your training to prepare your quads for Kilimanjaro's long descents.</p>
            </div>

            <h2>Altitude Preparation Strategies</h2>
            <p>Altitude presents the greatest challenge on Kilimanjaro, as the body must adapt to functioning with approximately 50% less oxygen at the summit compared to sea level.</p>

            <div class="image-with-caption">
                <img src="img/kilimanjaro-altitude-banner.jpg" alt="Altitude Training for Kilimanjaro">
                <p>Understanding altitude adaptation is crucial for Kilimanjaro success</p>
            </div>

            <h3>Pre-Acclimatization Options</h3>
            <p>While nothing fully replicates high altitude at sea level, these strategies can help:</p>
            
            <p><strong>Altitude Simulation:</strong> If available, training in an altitude chamber or using altitude simulation masks can provide some physiological adaptation. These tools reduce oxygen intake, forcing your body to produce more red blood cells.</p>
            
            <p><strong>Hypoxic Training:</strong> Some fitness centers offer hypoxic workouts where the training environment has reduced oxygen levels. Research shows this can provide modest benefits for altitude adaptation.</p>
            
            <p><strong>Natural Altitude Exposure:</strong> If possible, schedule training hikes at higher elevations before your trip. Even modest altitudes of 2,000-3,000 meters can stimulate beneficial physiological changes.</p>

