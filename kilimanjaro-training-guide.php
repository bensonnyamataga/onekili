            <h2>Sample Training Schedule</h2>
            <p>This 16-week progressive training plan builds the fitness needed for a successful Kilimanjaro climb:</p>

            <h3>Weeks 1-4: Foundation Building</h3>
            <p>Establish consistency with 3-4 cardio sessions weekly (30-45 minutes), 2 strength sessions, and one weekend hike (2-3 hours). Focus on proper form and gradual intensity increases.</p>

            <h3>Weeks 5-8: Endurance Development</h3>
            <p>Increase cardio sessions to 45-60 minutes, add intensity intervals, and extend weekend hikes to 3-4 hours with elevation gain. Incorporate pack carrying during hikes starting with 5kg and building to 8kg.</p>

            <h3>Weeks 9-12: Mountain Specificity</h3>
            <p>Introduce back-to-back training days to simulate consecutive climbing days. Weekend hikes should reach 5-6 hours with significant elevation (800+ meters) while carrying 8-10kg packs.</p>

            <h3>Weeks 13-16: Peak and Taper</h3>
            <p>Complete your longest training hikes (6-8 hours) in weeks 13-14, then gradually reduce volume in the final two weeks before your climb to allow for recovery and supercompensation.</p>

            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-calendar-check"></i>
                </div>
                <p>Consistency matters more than intensity when preparing for Kilimanjaro. Regular, moderate training spread over several months yields better results than intense, sporadic workouts that increase injury risk.</p>
            </div>

            <h2>Mental Preparation Techniques</h2>
            <p>The psychological challenge of Kilimanjaro is often underestimated. Summit night in particular tests mental fortitude as much as physical fitness.</p>

            <h3>Developing Mental Resilience</h3>
            <p>Incorporate these strategies into your preparation:</p>
            
            <p><strong>Visualization:</strong> Regularly visualize yourself successfully navigating challenging sections of the climb, particularly the summit push. Mental rehearsal builds neural pathways that support physical performance.</p>
            
            <p><strong>Break Down the Challenge:</strong> On the mountain, focus on reaching the next rest stop or landmark rather than the distant summit. Apply this same strategy during training by breaking long workouts into manageable segments.</p>
            
            <p><strong>Embrace Discomfort:</strong> During training, occasionally push through moderate discomfort to develop your tolerance. Learning to continue while tired, cold, or slightly uncomfortable builds the resilience needed for summit night.</p>

