            <h2>Difficulty Level and Physical Requirements</h2>
            <p>The Machame Route is considered moderately difficult, offering a challenging but achievable experience for well-prepared climbers.</p>

            <h3>Technical Difficulty</h3>
            <p>Machame presents some technical challenges but remains non-technical:</p>
            
            <p><strong>Trail Conditions:</strong> Varied terrain including steep sections, rocky paths, and one scrambling section</p>
            
            <p><strong>Barranco Wall:</strong> Fun scrambling section requiring use of hands but no technical climbing skills</p>
            
            <p><strong>Summit Night:</strong> Steep, sustained ascent to Stella Point with some scree sections</p>

            <h3>Physical Demands</h3>
            <p>The route requires good physical conditioning:</p>
            
            <p><strong>Summit Day/Night:</strong> 12-16 hours of continuous hiking with 1,200m ascent and descent</p>
            
            <p><strong>Daily Distances:</strong> 5-7 hours of hiking per day with significant elevation changes</p>
            
            <p><strong>Endurance:</strong> Multiple consecutive days of challenging hiking in variable conditions</p>

            <div class="highlight-box secondary">
                <div class="highlight-icon">
                    <i class="fas fa-hiking"></i>
                </div>
                <p><strong>Guide Insight:</strong> The Barranco Wall looks intimidating but is actually one of the most enjoyable parts of the climb. It's a scramble rather than a technical climb, and our guides provide assistance and instruction. Most climbers find it to be a highlight that breaks the monotony of straightforward hiking.</p>
            </div>

            <h2>Best Time to Climb the Machame Route</h2>
            <p>The Machame Route can be climbed year-round, with dry seasons offering optimal conditions for summit success and photography opportunities.</p>

            <div class="image-with-caption">
                <img src="img/machame-route-weather.jpg" alt="Best climbing seasons for Machame Route">
                <p>The dry seasons offer the best conditions for Machame Route climbs with clear views</p>
            </div>

            <h3>Optimal Climbing Seasons</h3>
            <p>The two dry seasons provide the best climbing conditions:</p>
            
            <p><strong>June to October (Long Dry Season):</strong> Clear skies, minimal precipitation, excellent visibility for photography. This is the most popular time with the highest success rates.</p>
            
            <p><strong>December to March (Short Dry Season):</strong> Warmer temperatures with generally good conditions. Beautiful clear days perfect for capturing Kilimanjaro's landscapes.</p>

            <h3>Shoulder Seasons</h3>
            <p>Transition periods with more variable conditions:</p>
            
            <p><strong>January-February:</strong> Warm and generally dry with excellent visibility</p>
            
            <p><strong>November:</strong> "Short rains" period with potential afternoon showers but spectacular cloud formations</p>

            <h3>Rainy Seasons</h3>
            <p>Challenging but rewarding for experienced climbers:</p>
            
            <p><strong>March to May (Long Rains):</strong> Heavy rainfall, muddy trails, and limited visibility. Fewer crowds and lush green landscapes.</p>
            
            <p><strong>Considerations:</strong> The camping experience requires excellent wet weather gear during rainy seasons, but the solitude can be rewarding for experienced adventurers.</p>

