                                <div class="safari-faq-question">
                                    <h4>Why is Machame called the "Whiskey" route?</h4>
                                    <span class="safari-faq-toggle">+</span>
                                </div>
                                <div class="safari-faq-answer">
                                    <p>The Machame Route earned the nickname "Whiskey" route as a contrast to Marangu's "Coca-Cola" route. While Marangu is considered easier with hut accommodation (like the soft drink Coca-Cola), Machame is more challenging and wilderness-focused (like the stronger beverage whiskey). The name reflects Machame's reputation as a more robust, adventurous climbing experience compared to the more comfortable Marangu option.</p>
                                </div>
                            </div>

                            <div class="safari-faq-item">
                                <div class="safari-faq-question">
                                    <h4>How difficult is the Barranco Wall on the Machame Route?</h4>
                                    <span class="safari-faq-toggle">+</span>
                                </div>
                                <div class="safari-faq-answer">
                                    <p>The Barranco Wall is a scrambling section rather than a technical climb. While it looks intimidating, most climbers find it to be one of the most enjoyable parts of the route. It involves using your hands for balance and support but doesn't require climbing equipment or technical skills. Our guides provide assistance and instruction, and the wall typically takes 1-2 hours to navigate. It's more of a fun challenge than a dangerous obstacle.</p>
                                </div>
                            </div>

                            <div class="safari-faq-item">
                                <div class="safari-faq-question">
                                    <h4>What makes the 7-day Machame itinerary better than the 6-day?</h4>
                                    <span class="safari-faq-toggle">+</span>
                                </div>
                                <div class="safari-faq-answer">
                                    <p>The 7-day Machame itinerary includes an extra acclimatization day with a hike from Shira Camp to Lava Tower (4,630m) followed by descent to Barranco Camp (3,960m). This "climb high, sleep low" strategy is crucial for proper altitude adaptation. The additional day increases summit success rates from approximately 75-80% on the 6-day itinerary to 85% on the 7-day itinerary. The extra day also makes the overall experience more enjoyable with shorter hiking days.</p>
                                </div>
                            </div>

                            <div class="safari-faq-item">
                                <div class="safari-faq-question">
                                    <h4>How cold does it get camping on the Machame Route?</h4>
                                    <span class="safari-faq-toggle">+</span>
                                </div>
                                <div class="safari-faq-answer">
                                    <p>Temperatures vary significantly by elevation. In the rainforest zone (Machame Camp), nighttime temperatures typically range from 5-10°C (41-50°F). At higher camps like Shira and Barranco, temperatures can drop to 0-5°C (32-41°F) at night. At Barafu Camp (base camp), nighttime temperatures often reach -5 to -10°C (23-14°F). We provide high-quality sleeping bags rated for these conditions, but proper clothing layers are essential for comfort.</p>
                                </div>
                            </div>

                            <div class="safari-faq-item">
                                <div class="safari-faq-question">
                                    <h4>Is the Machame Route more scenic than other routes?</h4>
                                    <span class="safari-faq-toggle">+</span>
                                </div>
                                <div class="safari-faq-answer">
                                    <p>Yes, the Machame Route is widely considered one of Kilimanjaro's most scenic routes. It offers diverse landscapes including lush rainforest, heath and moorland zones, the dramatic Shira Plateau, the iconic Lava Tower, and spectacular views of Kilimanjaro's southern ice fields. The route approaches the summit from the southwest, providing unique perspectives of the mountain that other routes don't offer. Many photographers choose Machame specifically for its photographic opportunities.</p>
                                </div>
                            </div>

                            <div class="safari-faq-item">
                                <div class="safari-faq-question">
                                    <h4>What's the success rate comparison between 6-day and 7-day Machame?</h4>
                                    <span class="safari-faq-toggle">+</span>
                                </div>
                                <div class="safari-faq-answer">
                                    <p>The success rate difference is significant: approximately 75-80% for the 6-day itinerary versus 85% for the 7-day itinerary. The extra acclimatization day on the 7-day route makes this substantial difference. The 6-day itinerary is more physically demanding with longer hiking days and less time for altitude adaptation. We strongly recommend the 7-day option for most climbers, reserving the 6-day itinerary for those with previous high-altitude experience and excellent fitness.</p>
                                </div>
