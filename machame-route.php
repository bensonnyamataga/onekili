            <h2>Accommodation on the Machame Route</h2>
            <p>The Machame Route is a camping experience, offering a true wilderness adventure with comfortable tent accommodation.</p>

            <h3>Camping Experience Overview</h3>
            <p>All camps provide well-organized camping facilities:</p>
            
            <p><strong>Sleeping Arrangements:</strong> Quality expedition tents typically shared by 2 people</p>
            
            <p><strong>Dining Facilities:</strong> Spacious dining tent with tables and chairs for meals</p>
            
            <p><strong>Cooking:</strong> Professional cook prepares nutritious meals in separate kitchen tent</p>
            
            <p><strong>Bathroom Facilities:</strong> Portable toilet tents provided at each campsite</p>

            <h3>Campsite-Specific Details</h3>
            <p>Each camp offers unique settings and experiences:</p>
            
            <p><strong>Machame Camp (3,000m):</strong> Forest clearing with sheltered location</p>
            
            <p><strong>Shira Camp (3,840m):</strong> Exposed plateau with spectacular sunset views</p>
            
            <p><strong>Barranco Camp (3,960m):</strong> Sheltered valley below the impressive Barranco Wall</p>
            
            <p><strong>Barafu Camp (4,673m):</strong> High-altitude base camp with stark, lunar landscape</p>

            <h3>What to Expect</h3>
            <p>Realistic expectations for the camping experience:</p>
            
            <p><strong>Comfort Level:</strong> Surprisingly comfortable with proper equipment and support team</p>
            
            <p><strong>Temperature:</strong> Cold nights requiring warm sleeping bags and clothing</p>
            
            <p><strong>Privacy:</strong> Good - individual tents provide personal space</p>
            
            <p><strong>Atmosphere:</strong> Adventurous and communal with campfire camaraderie</p>

