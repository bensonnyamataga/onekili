            <h2>Sample 7-Day Kilimanjaro Menu</h2>
            <p>This representative menu shows the variety and nutritional strategy across a typical week-long climb.</p>

            <h3>Day 1: Machame Gate to Machame Camp (3,000m)</h3>
            <p><strong>Breakfast:</strong> Scrambled eggs, sausage, toast with jam, porridge with honey, fresh fruit, coffee/tea</p>
            <p><strong>Lunch:</strong> Chicken sandwich, carrot sticks, banana, chocolate biscuit</p>
            <p><strong>Dinner:</strong> Pumpkin soup, grilled chicken with rice and vegetables, fresh fruit salad</p>

            <h3>Day 3: Shira Camp to Barranco Camp (3,900m)</h3>
            <p><strong>Breakfast:</strong> Pancakes with honey, baked beans, porridge with raisins, coffee/tea</p>
            <p><strong>Lunch:</strong> Cheese sandwich, apple, granola bar, boiled egg</p>
            <p><strong>Dinner:</strong> Vegetable soup, beef stew with ugali and sukuma wiki, custard with banana</p>

            <h3>Day 5: Karanga Camp to Barafu Camp (4,673m)</h3>
            <p><strong>Breakfast:</strong> Oatmeal with nuts, toast with peanut butter, hot chocolate</p>
            <p><strong>Lunch:</strong> Energy bars, crackers with cheese, orange, soup</p>
            <p><strong>Dinner:</strong> Lentil soup, spaghetti bolognese, canned fruit</p>

            <h3>Summit Day: Barafu to Uhuru Peak (5,895m)</h3>
            <p><strong>Midnight Snack:</strong> Biscuits, hot sweet tea, energy gels</p>
            <p><strong>Summit Celebration:</strong> Hot juice or tea at Stella Point</p>
            <p><strong>Post-Summit Lunch:</strong> Rehydration drinks, light soup, simple carbohydrates</p>

