            <p>Nutrition plays a vital role in your Kilimanjaro success story. At high altitude, your body works harder, burns more calories, and faces unique digestive challenges. Our experienced mountain chefs craft menus that provide the necessary 3,000-5,000 daily calories while accommodating altitude-induced appetite changes. From hearty breakfasts that launch your day to celebratory summit dinners, every meal is designed to fuel your body and lift your spirits throughout this incredible journey.</p>

            <h2>The Science of High-Altitude Nutrition</h2>
            <p>Understanding how altitude affects your body's nutritional needs is key to appreciating our carefully planned menus. As you ascend, several physiological changes occur that impact digestion and metabolism.</p>
            
            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-utensils"></i>
                </div>
                <p>Climbers can burn 4,000-6,000 calories daily on Kilimanjaro, yet many experience 30-50% appetite reduction at altitude. Our chefs overcome this paradox through strategic menu planning that maximizes nutritional density in appealing, easily digestible formats.</p>
            </div>

            <p>Key nutritional challenges at altitude include:
            <ul>
                <li>Increased energy expenditure due to physical exertion and thermoregulation</li>
                <li>Reduced appetite and altered taste perception</li>
                <li>Impaired digestion and nutrient absorption</li>
                <li>Higher risk of dehydration affecting overall metabolism</li>
                <li>Increased carbohydrate utilization for energy</li>
            </ul>
            </p>

            <h2>Breakfast: Fueling Your Day on the Mountain</h2>
            <p>Mountain breakfasts are designed to provide sustained energy for 4-7 hours of hiking while being appealing even when appetites are suppressed.</p>

            <div class="image-with-caption">
                <img src="img/kilimanjaro-foods-trekking.jpg" alt="Kilimanjaro breakfast spread">
                <p>Hearty breakfasts provide the foundation for successful hiking days on Kilimanjaro</p>
            </div>

            <h3>Typical Breakfast Menu</h3>
            <p>Each morning begins with a warming beverage and progresses to a substantial meal:</p>
            
            <p><strong>Beverage Selection:</strong> Choice of coffee, tea (black, ginger, or chai), hot chocolate, or Milo. Herbal teas and ginger tea are particularly popular for settling stomachs.</p>
            
            <p><strong>Porridge Variations:</strong> Creamy oatmeal or porridge made with milk powder, often enhanced with cinnamon, honey, dried fruits, or ground nuts for extra calories and flavor.</p>
            
            <p><strong>Protein Options:</strong> Scrambled eggs, sausages, or baked beans provide essential protein for muscle maintenance and satiety.</p>
            
            <p><strong>Carbohydrate Sources:</strong> Toast with jam or honey, pancakes, or chapati offer easily digestible carbohydrates for immediate energy.</p>
            
            <p><strong>Fresh Fruits:</strong> When available, bananas, oranges, or passion fruit provide vitamins and natural sugars.</p>

            <h3>Altitude Adaptation Strategy</h3>
            <p>As you ascend, breakfast menus subtly adapt to changing digestive capabilities:</p>
            
            <p><strong>Lower Altitudes (1,800-3,000m):</strong> Full breakfast spreads with multiple protein and carbohydrate options.</p>
            
            <p><strong>Middle Altitudes (3,000-4,500m):</strong> Simplified menus focusing on easily digestible carbohydrates with reduced fat content.</p>
            
            <p><strong>High Altitudes (4,500m+):</strong> Liquid-heavy options like porridge and nutrient-dense drinks for summit day preparation.</p>

