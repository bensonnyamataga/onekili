            <h2>Lunch: Energy Maintenance on the Trail</h2>
            <p>Lunch on Kilimanjaro is typically a packed affair eaten during a scenic break along the trail, designed for convenience and rapid energy replenishment.</p>

            <h3>Standard Lunch Package</h3>
            <p>Our lunch boxes balance nutrition, portability, and appeal:</p>
            
            <p><strong>Sandwich Selection:</strong> Freshly made sandwiches with various fillings—cheese, chicken, egg, or vegetable—on whole wheat or white bread.</p>
            
            <p><strong>Energy Snacks:</strong> Granola bars, biscuits, cookies, or energy balls provide quick-release carbohydrates.</p>
            
            <p><strong>Fresh Components:</strong> Piece of fruit (apple, orange, or banana), carrot sticks, or cucumber slices add freshness and vitamins.</p>
            
            <p><strong>Sweet Treats:</strong> Muffins, cake slices, or chocolate bars offer mental boost and additional calories.</p>
            
            <p><strong>Hot Supplement:</strong> On colder days or at higher camps, soup is often served alongside lunch boxes.</p>

            <div class="highlight-box secondary">
                <div class="highlight-icon">
                    <i class="fas fa-apple-alt"></i>
                </div>
                <p><strong>Chef's Tip:</strong> Many climbers find they prefer eating smaller amounts more frequently rather than a single large lunch. Don't hesitate to save parts of your lunch box for grazing throughout the afternoon hike. Listening to your body's signals is more important than finishing everything at once.</p>
            </div>

            <h2>Dinner: Recovery and Preparation</h2>
            <p>Evening meals focus on replenishing energy stores, aiding muscle recovery, and preparing your body for the next day's challenges.</p>

            <div class="image-with-caption">
                <img src="img/Kilimanjaro-Food.jpg" alt="Kilimanjaro dinner meal">
                <p>Nutrient-dense dinners support recovery and preparation for upcoming altitude gains</p>
            </div>

            <h3>Typical Dinner Menu Structure</h3>
            <p>Dinners follow a three-course format to ensure comprehensive nutrition:</p>
            
            <p><strong>Starter Course:</strong> Warming soup—cream of vegetable, pumpkin, tomato, or lentil—that aids hydration and stimulates appetite.</p>
            
            <p><strong>Main Course:</strong> Balanced plate with carbohydrate base (rice, pasta, or potatoes), protein source (chicken, beef, or fish), and vegetable component.</p>
            
            <p><strong>Dessert:</strong> Simple sweets like fresh fruit, custard, or cake provide psychological comfort and additional calories.</p>

            <h3>Popular Dinner Entrees</h3>
            <p>Our chefs rotate through climber-favorite dishes:</p>
            
            <p><strong>African Specialties:</strong> Traditional dishes like ugali (maize porridge) with stew, or pilau rice with grilled meat introduce local flavors.</p>
            
            <p><strong>International Favorites:</strong> Spaghetti bolognese, fried chicken with rice, or fish with mashed potatoes offer familiar comfort foods.</p>
            
            <p><strong>Vegetarian Options:</strong> Lentil curry, vegetable stir-fry, or bean stews provide plant-based protein and fiber.</p>

