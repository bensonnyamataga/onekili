                <div class="highlight-icon">
                    <i class="fas fa-concierge-bell"></i>
                </div>
                <p>Our chefs adjust menus in real-time based on group appetite, weather conditions, and altitude responses. Don't be surprised if you find unexpected treats or menu variations—these adjustments are part of our strategy to ensure every climber gets the nutrition they need in the most appealing format possible.</p>
            </div>

            <h2>Special Dietary Requirements</h2>
            <p>We successfully accommodate various dietary needs with advance notice, ensuring all climbers receive proper nutrition.</p>

            <h3>Common Accommodations</h3>
            <p>Our kitchen team regularly prepares for these dietary requirements:</p>
            
            <p><strong>Vegetarian:</strong> Plant-based protein sources including lentils, beans, eggs, and dairy products with increased vegetable variety.</p>
            
            <p><strong>Vegan:</strong> Comprehensive plant-based menus using local vegetables, grains, and legumes with careful attention to protein complementarity.</p>
            
            <p><strong>Gluten-Free:</strong> Alternative grains like rice, maize, and potatoes with gluten-free bread and pasta options.</p>
            
            <p><strong>Lactose Intolerant:</strong> Dairy-free preparations using oil instead of butter, and lactose-free milk powder for beverages.</p>
            
            <p><strong>Religious Dietary Laws:</strong> Halal and Kosher options with proper ingredient sourcing and preparation protocols.</p>

            <h3>Communication is Key</h3>
            <p>For successful dietary accommodation:</p>
            
            <p><strong>Advanced Notice:</strong> Inform us of dietary requirements at least 30 days before your climb for proper provisioning.</p>
            
            <p><strong>Specific Details:</strong> Provide detailed information about restrictions, preferences, and severity of conditions.</p>
            
            <p><strong>Personal Snacks:</strong> Bring specialized snacks or supplements for peace of mind, though our chefs can accommodate most needs.</p>

            <h2>Snacks and Personal Nutrition Supplements</h2>
            <p>While we provide comprehensive meals, many climbers benefit from personal snacks that match their taste preferences.</p>

            <h3>Recommended Personal Snacks</h3>
            <p>Consider bringing these snack categories:</p>
            
            <p><strong>Quick Energy:</strong> Energy gels, chews, or blocks for rapid carbohydrate delivery during strenuous sections.</p>
            
            <p><strong>Nuts and Seeds:</strong> High-calorie, nutrient-dense options like almonds, cashews, or trail mix.</p>
            
            <p><strong>Dried Fruits:</strong> Dates, apricots, or raisins for natural sugars and potassium.</p>
            
            <p><strong>Comfort Foods:</strong> Small treats like your favorite chocolate or candy for psychological boost.</p>
            
            <p><strong>Electrolyte Supplements:</strong> Individual electrolyte tablets or powders for personalized hydration management.</p>

