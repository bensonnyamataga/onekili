            <h2>The Night Before Summit: A Symphony of Anticipation and Anxiety</h2>
            <p>The evening at Barafu or Kosovo Camp (depending on your route) carries a unique electricity. There's a tangible shift in energy among climbers—a mixture of excitement, anxiety, and determination that hangs in the thin air. Dinner is quiet, contemplative. Most climbers pick at their food, minds already ascending the mountain ahead of their bodies. The guides move with purposeful calm, checking gear, offering quiet words of encouragement, and sharing final summit strategies.</p>

            <p>As you retreat to your tent, the reality sets in: in just a few hours, you'll begin the most challenging physical endeavor of your life. The cold seeps through the tent fabric, a preview of what's to come. Sleep is fragmented at best—a series of dozes interrupted by racing thoughts and the occasional sound of other climbers preparing. You check and recheck your summit gear: headlamp, insulated layers, gloves, water bladders. Each item feels charged with significance. There's a profound sense of standing on the edge of something monumental, knowing that the next time you try to sleep, you'll either be a summiter or someone who turned back.</p>

            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-moon"></i>
                </div>
                <p>The summit night ritual is universal across all routes: a 10-11 PM wake-up call, forcing down some tea and biscuits despite nerves, layered dressing in near-total darkness, and that final moment when you step out of camp into the ink-black night, your headlamp beam cutting a small circle of visibility in an immense darkness.</p>
            </div>

            <h2>The Summit Night Ascent: Pole Pole Through Darkness</h2>
            <p>When you begin walking just before midnight, the world reduces to the circle of light from your headlamp, the sound of your own breathing, and the crunch of volcanic scree underfoot. The famous Swahili phrase "pole pole" (slowly slowly) takes on sacred meaning. In the darkness, there's no sense of progress, no visible goal—just one foot in front of the other, following the boots of the person ahead, trusting your guide completely.</p>

            <p>The first hour feels almost manageable. The excitement carries you. But as you ascend, several realities converge. The cold becomes a tangible presence, creeping through layers despite your preparation. Breathing becomes a conscious act—deliberate inhalations of thin air that never seem quite sufficient. The steepness of the trail surprises many climbers; this is not a gentle incline but a relentless upward grind.</p>

            <p>Around 2 AM, the mental challenge truly begins. The novelty has worn off, fatigue sets in, and the summit still feels impossibly distant. This is when climbers confront their inner dialogues. Some bargain with themselves: "Just to the next rock." Others repeat mantras. Some focus on breathing patterns. Many experience moments of doubt so profound they consider turning back. The guides, perceptive to these struggles, offer encouragement, sometimes physically assisting by carrying a daypack or offering an arm for support.</p>

            <div class="image-with-caption">
