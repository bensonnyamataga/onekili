            <h2>Footwear: Your Most Critical Equipment</h2>
            <p>Proper footwear can prevent blisters, provide stability on uneven terrain, and keep your feet warm during the frigid summit push.</p>

            <div class="image-with-caption">
                <img src="img/what-type-of-hiking-boots.jpg" alt="Proper Kilimanjaro hiking boots">
                <p>Investing in quality broken-in boots is essential for comfort and safety on Kilimanjaro</p>
            </div>

            <h3>Mountaineering Boots</h3>
            <p>Your primary footwear for the entire climb should be B2 or B3 rated mountaineering boots:</p>
            
            <p><strong>Insulation and Warmth:</strong> Look for boots with integrated insulation (at least 200g) and waterproof/breathable membranes. Your boots should be rated for -20°C to -30°C conditions.</p>
            
            <p><strong>Ankle Support:</strong> High-cut boots provide crucial ankle support on rocky, uneven terrain and when carrying a loaded pack.</p>
            
            <p><strong>Break-in Period:</strong> Start breaking in new boots at least 2-3 months before your climb. Wear them on training hikes with your loaded daypack to ensure proper fit.</p>

            <h3>Camp Shoes and Additional Footwear</h3>
            <p>Don't underestimate the importance of camp comfort:</p>
            
            <p><strong>Camp Shoes:</strong> Lightweight sneakers, approach shoes, or sturdy sandals give your feet a break from boots and are essential for moving around camp.</p>
            
            <p><strong>Gaiters:</strong> Knee-high waterproof gaiters prevent snow, scree, and mud from entering your boots, particularly important during the summit descent.</p>
            
            <p><strong>Extra Socks:</strong> Bring 4-5 pairs of hiking socks (merino wool or synthetic blend) plus 1-2 pairs of dedicated summit socks.</p>

            <div class="highlight-box secondary">
                <div class="highlight-icon">
                    <i class="fas fa-socks"></i>
                </div>
                <p><strong>Pro Tip:</strong> Use a two-sock system for summit night: a thin synthetic liner sock under a thick wool hiking sock. This combination reduces friction and provides maximum warmth while wicking moisture away from your feet.</p>
            </div>

            <h2>Headwear and Hand Protection</h2>
            <p>Significant body heat is lost through the head and hands, making proper protection essential for comfort and safety.</p>

            <div class="image-with-caption">
                <img src="img/kili-gloves.jpg" alt="Cold weather head and hand protection">
                <p>Proper head and hand protection is crucial for managing body temperature in extreme conditions</p>
            </div>

            <h3>Headwear System</h3>
            <p>A multi-layer approach to head protection ensures adaptability to changing conditions:</p>
            
            <p><strong>Base Layer:</strong> A synthetic or wool beanie that fits comfortably under your hoods. Consider a balaclava for summit night for full face protection.</p>
            
            <p><strong>Mid Layer:</strong> A fleece or insulated hat with ear protection for colder conditions at higher camps.</p>
            
            <p><strong>Outer Layer:</strong> Your jacket hood provides the final wind and weather protection layer.</p>
            
            <p><strong>Sun Protection:</strong> A wide-brimmed sun hat and neck gaiter are essential for protection against intense equatorial sun at lower elevations.</p>

            <h3>Glove System</h3>
            <p>Like your clothing system, gloves should follow a layering approach:</p>
            
            <p><strong>Liner Gloves:</strong> Thin synthetic gloves for cool mornings and dexterity needs like operating camera equipment or adjusting gear.</p>
            
            <p><strong>Insulated Gloves:</strong> Medium-weight insulated gloves for general hiking in cold conditions. Look for models with touchscreen compatibility.</p>
            
            <p><strong>Summit Mittens:</strong> Heavy-duty expedition mittens with waterproof shells for summit night. Mittens provide superior warmth compared to gloves by keeping fingers together.</p>

