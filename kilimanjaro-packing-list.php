            <p>Packing for Mount Kilimanjaro requires careful consideration of the mountain's unique challenges: five distinct climate zones, temperature extremes from +25°C to -25°C, and rapidly changing weather conditions. This comprehensive packing guide covers every essential item you'll need for a safe, comfortable, and successful climb. Remember that while porters carry most of your gear, your daypack containing critical items stays with you at all times.</p>

            <h2>The Layering System: Your Foundation for Comfort</h2>
            <p>Understanding the layering system is crucial for managing Kilimanjaro's extreme temperature variations. This system allows you to add or remove layers as conditions change throughout the day and across different altitude zones.</p>
            
            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-layer-group"></i>
                </div>
                <p>Proper layering can reduce heat loss by up to 40% compared to wearing a single heavy layer. The moisture-wicking, insulating, and protective functions of each layer work together to maintain optimal body temperature in changing conditions.</p>
            </div>

            <h3>Base Layers: Moisture Management</h3>
            <p>Your base layer sits directly against your skin and is responsible for moisture management:</p>
            
            <p><strong>Material Choices:</strong> Merino wool or synthetic fabrics like polyester or polypropylene are ideal. Avoid cotton completely as it retains moisture and loses insulating properties when wet.</p>
            
            <p><strong>Quantity Recommendations:</strong> Bring 2-3 sets of base layers. You'll want a fresh set for summit night and alternates for the other days of your climb.</p>
            
            <p><strong>Fit Considerations:</strong> Base layers should fit snugly but not restrict movement. Look for flatlock seams to prevent chafing during long hiking days.</p>

            <h3>Mid Layers: Insulation</h3>
            <p>Mid layers provide insulation by trapping warm air close to your body:</p>
            
            <p><strong>Fleece Jacket:</strong> A medium-weight fleece (200-300 weight) offers excellent warmth-to-weight ratio and maintains insulation when damp.</p>
            
            <p><strong>Insulated Jacket:</strong> A down or synthetic insulated jacket is essential for camp wear and summit night. Down offers superior warmth-to-weight but loses insulation when wet, while synthetic insulation performs better in damp conditions.</p>
            
            <p><strong>Versatile Options:</strong> Consider a grid-fleece hoody or insulated vest for additional layering options that provide core warmth without bulk.</p>

            <h3>Outer Layers: Weather Protection</h3>
            <p>Your outer shell protects against wind, rain, and snow:</p>
            
            <p><strong>Waterproof Jacket:</strong> A high-quality Gore-Tex or equivalent waterproof/breathable shell is non-negotiable. Look for fully taped seams, waterproof zippers, and adjustable cuffs and hood.</p>
            
            <p><strong>Waterproof Pants:</strong> Similarly, waterproof overtrousers with full-length side zippers allow easy putting on and removal over boots and other layers.</p>
            
            <p><strong>Features Matter:</strong> Pit zips for ventilation, multiple pockets with storm flaps, and adjustable hood systems that accommodate helmets are worth the investment.</p>

