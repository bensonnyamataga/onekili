            <h2>Daypack Essentials: What to Carry Daily</h2>
            <p>Your daypack should contain everything you need for each day's hike, as you won't have access to your main duffel until camp.</p>

            <h3>Critical Daypack Items</h3>
            <p>These items should never leave your daypack during hiking hours:</p>
            
            <p><strong>Rain Gear:</strong> Your waterproof jacket and pants should be readily accessible, not buried at the bottom of your pack.</p>
            
            <p><strong>Warm Layers:</strong> Your fleece and insulated jacket for sudden temperature drops or rest stops.</p>
            
            <p><strong>Hydration:</strong> All your water for the day's hike, plus water purification tablets as backup.</p>
            
            <p><strong>Snacks:</strong> High-energy snacks to maintain blood sugar levels throughout the day.</p>
            
            <p><strong>First Aid:</strong> Personal first aid kit including blister treatment, medications, and any personal prescriptions.</p>
            
            <p><strong>Sun Protection:</strong> Sunscreen, lip balm with SPF, and sunglasses with UV protection.</p>
            
            <p><strong>Headlamp:</strong> Even for day hikes, in case of delays that extend into darkness.</p>
            
            <p><strong>Camera:</strong> With extra batteries, as cold drains battery life rapidly.</p>

            <h3>Summit Night Specific Gear</h3>
            <p>Prepare a separate summit pack within your daypack for the final push:</p>
            
            <p><strong>Extra Warm Layers:</strong> Your warmest base layers, insulated jacket, and summit mittens.</p>
            
            <p><strong>Additional Nutrition:</strong> Easily accessible high-energy foods that won't freeze solid.</p>
            
            <p><strong>Hot Liquids:</strong> Insulated thermos with hot tea, coffee, or soup for the summit push.</p>
            
            <p><strong>Hand Warmers:</strong> Chemical hand and toe warmers for extra comfort during the coldest hours.</p>

            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-hiking"></i>
                </div>
                <p>Your daypack weight should not exceed 30% of your body weight. A properly packed daypack feels balanced and doesn't pull you backward. Test your packed weight during training hikes to ensure comfort over long distances.</p>
            </div>

            <h2>Duffel Bag Organization and Porter Guidelines</h2>
            <p>Understanding how to pack your duffel bag properly ensures your gear arrives dry and organized at each camp.</p>

            <h3>Duffel Bag Specifications</h3>
            <p>Follow these guidelines for your main luggage:</p>
            
            <p><strong>Size Limitations:</strong> Most operators specify 15-20kg weight limit including your sleeping bag and mattress. Use a waterproof duffel of 90-120 liters capacity.</p>
            
            <p><strong>Waterproofing:</strong> Line your duffel with a heavy-duty trash compactor bag or use dedicated waterproof stuff sacks for critical items like sleeping bags and clothing.</p>
            
            <p><strong>Organization:</strong> Use packing cubes or different colored stuff sacks to organize clothing by type or day for easy access.</p>

            <h3>Porter Considerations</h3>
            <p>Respect the porters who make your climb possible:</p>
            
            <p><strong>Weight Distribution:</strong> Pack heavier items at the bottom and center of your duffel for better balance when carried on heads or backs.</p>
            
            <p><strong>Accessibility:</strong> Items you might need during the day (like camp shoes or extra layers) should be packed last for easy retrieval by porters at camp.</p>
            
            <p><strong>Cultural Sensitivity:</strong> Avoid packing unnecessary luxury items that add significant weight to porter loads.</p>

            <h2>Rental Equipment vs. Personal Gear</h2>
            <p>Deciding what to bring versus what to rent requires balancing cost, quality, and future use considerations.</p>

            <h3>Recommended Rental Items</h3>
            <p>These items are often better to rent unless you plan future high-altitude expeditions:</p>
            
            <p><strong>Sleeping Bag:</strong> High-altitude sleeping bags (-20°C to -30°C rating) are expensive and bulky. Quality rental bags are typically well-maintained and appropriate for conditions.</p>
            
            <p><strong>Sleeping Mat:</strong> Insulated sleeping mats are essential for comfort and warmth. Rental options are usually high-quality and tested for leaks.</p>
            
            <p><strong>Trekking Poles:</strong> While many climbers prefer their own, rental poles are available and eliminate airline transportation issues.</p>
            
            <p><strong>Down Suit:</strong> Some operators offer down suits for extreme cold on summit night, which few climbers would use again.</p>

            <h3>Worthwhile Personal Investments</h3>
            <p>These items are worth purchasing for quality, fit, and future use:</p>
            
            <p><strong>Boots:</strong> Properly fitted, broken-in boots are the most important personal equipment item.</p>
            
            <p><strong>Daypack:</strong> A comfortable, properly fitting daypack that you've tested on training hikes.</p>
            
            <p><strong>Clothing Layers:</strong> Base and mid layers that fit well and suit your personal temperature regulation needs.</p>
            
            <p><strong>Footwear:</strong> Camp shoes and socks that you know are comfortable.</p>

