            <h2>Altitude Medications and Prevention</h2>
            <p>Altitude sickness represents the most significant medical challenge on Kilimanjaro. Understanding prevention strategies and having appropriate medications can dramatically improve your summit chances and safety. There are three main types of altitude illness to understand: Acute Mountain Sickness (AMS), High Altitude Pulmonary Edema (HAPE), and High Altitude Cerebral Edema (HACE).</p>

            <h3>Acetazolamide (Diamox) for AMS Prevention</h3>
            <p>Acetazolamide is the most commonly prescribed medication for preventing Acute Mountain Sickness. It works by increasing ventilation and accelerating acclimatization. The typical prophylactic dose is 125mg twice daily starting one day before ascent and continuing until two days after reaching maximum altitude or starting descent.</p>
            
            <p>Common side effects include increased urination, tingling in fingers and toes, and altered taste of carbonated beverages. These are generally mild and manageable. Acetazolamide is a prescription medication in most countries and requires consultation with a doctor who understands its use for altitude prevention. It's contraindicated for people with sulfa allergies, severe kidney disease, or certain other conditions.</p>
            
            <p>While effective for prevention, Acetazolamide doesn't treat established altitude sickness. If symptoms develop despite prophylaxis, descent remains the primary treatment. Many experienced climbers and guides recommend trying altitude without medication first for lower climbs, but for Kilimanjaro's rapid ascent profile, prophylaxis is often recommended.</p>

            <h3>Dexamethasone for Treatment</h3>
            <p>Dexamethasone is a steroid medication used to treat severe AMS, HACE, and HAPE when immediate descent isn't possible. It reduces brain swelling and can be lifesaving in emergency situations. It should never be used as prophylaxis—only for treatment of established altitude illness under medical guidance.</p>
            
            <p>The typical dose for altitude illness treatment is 4mg every 6 hours. It should only be carried and used by those trained in its administration, which is why our guides carry it as part of their emergency medical supplies. Dexamethasone masks symptoms rather than curing the underlying problem, so descent remains essential even after administration.</p>

