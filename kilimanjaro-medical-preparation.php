            <h2>First Aid Supplies for Personal Kit</h2>
            <p>While our guides carry comprehensive group first aid kits, having personal supplies addresses minor issues quickly without needing to access the group kit. Your personal first aid items should focus on blisters, minor cuts, and personal comfort issues.</p>

            <h3>Wound Care Supplies</h3>
            <p>Blisters represent the most common trekking injury, and proper prevention and treatment keeps you walking comfortably. Moleskin or blister pads are essential for both prevention and treatment. Athletic tape or medical tape secures dressings. Antibiotic ointment (like Neosporin) prevents infection in minor cuts or open blisters.</p>

            <p>Antiseptic wipes clean wounds before dressing. Assorted bandages in various sizes cover small cuts and abrasions. Butterfly bandages or steri-strips can close small cuts that might otherwise require stitches. Gauze pads and rolls dress larger wounds. A small pair of medical scissors cuts tape and dressings. Tweezers remove splinters or debris from wounds.</p>

            <h3>Support and Comfort Items</h3>
            <p>Beyond wound care, several items address common discomforts that can detract from your trekking experience. Lip balm with SPF protection prevents chapped lips from sun and wind. High-SPF sunscreen (SPF 50+) is essential at altitude where UV exposure is extreme. Hand sanitizer maintains hygiene when washing isn't possible.</p>

            <p>Eye drops relieve dryness from dust and wind. Ear plugs ensure restful sleep in communal tent situations. Insect repellent is needed for lower elevations and pre/post-trek activities. Personal thermometer allows you to monitor for fever. Digital pulse oximeter (optional but recommended) lets you monitor your oxygen saturation and pulse rate throughout the climb.</p>

            <h2>Prescription Medications Management</h2>
            <p>If you take regular prescription medications, careful planning ensures continuity throughout your trek. Bring at least one week's extra supply beyond your planned trip duration in case of delays. Divide medications between your carry-on and checked luggage in case bags are delayed or lost.</p>

            <p>Carry medications in their original pharmacy containers with clear labels showing your name, medication name, dosage, and prescribing doctor. Have a letter from your doctor explaining your medical conditions and medications, especially if you're carrying controlled substances or injectable medications. Know the generic names of your medications in case you need replacements internationally.</p>

            <p>Consider time zone changes when scheduling medication times. For medications requiring refrigeration (like some insulin), discuss storage solutions with your doctor and our team—we can arrange cool storage in certain circumstances. If you use an inhaler for asthma, bring at least two and ensure they're not near expiration.</p>

            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-pills"></i>
                </div>
                <p>For travelers with chronic conditions like diabetes, asthma, or heart conditions, we recommend additional preparation. Schedule a pre-trip consultation with your specialist, obtain extra prescriptions, and consider medical alert jewelry. Our guides receive training in common emergency responses but need to know about your conditions to provide appropriate support.</p>
            </div>

            <h2>Dental and Vision Considerations</h2>
            <p>Minor dental or vision issues can become major problems on the mountain. Schedule a dental checkup before your trip to address any potential issues. If you're prone to dental problems, ask your dentist for temporary filling material and clove oil (for toothache pain) to include in your kit.</p>

            <p>For vision needs, bring an extra pair of glasses or contacts. If you wear contacts, consider daily disposables to avoid cleaning challenges. Have prescription sunglasses or clip-ons for glacier travel. Include lens cleaning solution and case even if you don't normally use it—you might need to remove contacts if you develop eye irritation.</p>

            <h2>Medical Documentation and Insurance</h2>
            <p>Proper documentation ensures you receive appropriate care if needed and facilitates insurance claims. Carry copies of your vaccination records, especially Yellow Fever if required. Have a list of your medications, allergies, and medical conditions in both English and a translation app or card in Swahili.</p>

            <p>Include emergency contact information both digitally and on paper. Carry your insurance card and details of your travel insurance policy, including the emergency assistance phone number. Know what your insurance covers regarding altitude evacuation—many standard policies exclude this, requiring specific adventure travel coverage.</p>

            <p>Consider joining a remote medical assistance service like Global Rescue or Medjet if your insurance doesn't provide adequate mountain evacuation coverage. These services specialize in extraction from remote locations and can coordinate with local providers for appropriate care.</p>

            <h2>Guide Medical Support and Emergency Protocols</h2>
            <p>Our Kilimanjaro guides receive extensive wilderness first aid training specific to high-altitude environments. All lead guides carry comprehensive group medical kits including emergency medications, wound care supplies, and diagnostic equipment like pulse oximeters and blood pressure cuffs.</p>

            <p>The group kit includes emergency oxygen for altitude illness treatment, a portable hyperbaric chamber (Gamow bag) for severe altitude cases, and emergency communications equipment. Guides monitor climbers daily for signs of altitude illness using established protocols like the Lake Louise Scoring System.</p>

            <p>Emergency evacuation plans are established for each route, with known helicopter landing zones and ground evacuation options. Guides maintain constant communication with our base operations team who can coordinate additional support if needed. Porters are trained in basic first aid and emergency carrying techniques for patient transport.</p>

