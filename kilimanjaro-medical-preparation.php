                <p>Schedule your travel medicine appointment 6-8 weeks before departure. Many vaccines require multiple doses over several weeks, and some medications need to be started before travel. Last-minute preparations often mean incomplete protection.</p>
            </div>

            <h2>Essential Vaccinations for Tanzania Travel</h2>
            <p>Tanzania has specific vaccination requirements and recommendations that vary based on your travel plans, length of stay, and current health status. While no vaccines are officially required for entry from most countries (except Yellow Fever if arriving from a Yellow Fever endemic country), several are strongly recommended for your protection.</p>

            <div class="image-with-caption">
                <img src="img/vaccination-certificate.jpg" alt="International Certificate of Vaccination">
                <p>Keep your vaccination records accessible throughout your travels—digital copies on your phone plus physical copies in your carry-on luggage</p>
            </div>

            <h3>Routine Vaccinations (Ensure Up-to-Date)</h3>
            <p>Before considering travel-specific vaccines, ensure your routine immunizations are current. These protect against diseases that remain common in many parts of the world, including Tanzania. Your doctor will review your immunization history and update any that have expired or were never completed.</p>
            
            <p>Measles, mumps, and rubella (MMR) protection is particularly important as outbreaks occur periodically. Tetanus, diphtheria, and pertussis (Tdap) should be current, with a booster within the last ten years. Influenza vaccination is recommended, especially if traveling during Tanzania's rainy seasons when respiratory illnesses are more common. Varicella (chickenpox) protection is essential if you never had the disease or vaccination. Polio vaccination should be verified, with adults receiving a one-time booster if never vaccinated as an adult.</p>
            
            <p>Pneumococcal vaccine may be recommended for older adults or those with certain medical conditions. COVID-19 vaccination should follow current international travel recommendations and Tanzania entry requirements at the time of your travel.</p>

            <h3>Travel-Specific Recommended Vaccinations</h3>
            <p>In addition to routine vaccines, several travel-specific vaccinations provide important protection against diseases more common in Tanzania than in your home country. These recommendations come from both the CDC and WHO for travelers to East Africa.</p>
            
            <p>Hepatitis A vaccination is highly recommended for all travelers to Tanzania. The virus can be contracted through contaminated food or water, regardless of where you eat or stay. Typhoid vaccination is advised for most travelers, especially those visiting smaller cities or rural areas, or adventurous eaters. Hepatitis B vaccination is recommended for travelers who might have sexual contact with new partners, get tattoos or piercings, or have any medical procedures.</p>
            
            <p>Yellow Fever vaccination may be required if you're arriving from a country with risk of Yellow Fever transmission. Even if not required, it's recommended for travelers visiting areas below 2,300 meters in Tanzania. Rabies vaccination is recommended for travelers involved in outdoor activities (like trekking) that might bring them into contact with animals, or those spending extended time in rural areas. Meningococcal meningitis vaccination may be recommended if traveling during dry season (December through June) when outbreaks are more common.</p>

