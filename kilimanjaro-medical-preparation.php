            <h2>Personal Medication Kit Checklist</h2>
            <p>Beyond prescription medications, a well-stocked personal medical kit addresses common issues that can develop during your trek. Pack medications in their original containers with clear labels. Divide them between your daypack and main duffel so you have essentials accessible during hiking hours.</p>

            <div class="image-with-caption">
                <img src="img/medical-kit.jpg" alt="Organized personal medical kit for trekking">
