            <h3>Zero Plastic Policy</h3>
            <p>We've eliminated single-use plastics from all our expeditions:</p>
            
            <p><strong>Water Purification Systems:</strong> Instead of bottled water, we provide advanced water purification systems at all camps, allowing climbers to safely refill reusable bottles.</p>
            
            <p><strong>Biodegradable Packaging:</strong> All food is packed in biodegradable or reusable containers, eliminating plastic waste from our operations.</p>
            
            <p><strong>Eco-Friendly Amenities:</strong> We provide biodegradable soaps, shampoos, and cleaning products that minimize environmental impact.</p>

            <h3>Carbon-Neutral Expeditions</h3>
            <p>We offset the carbon footprint of every expedition through verified environmental projects:</p>
            
            <p><strong>Reforestation Initiatives:</strong> For every climber, we plant indigenous trees in the Kilimanjaro foothills to combat deforestation and support local ecosystems.</p>
            
            <p><strong>Renewable Energy:</strong> Our base operations utilize solar power, and we're transitioning to portable solar systems for camp lighting and communication equipment.</p>
            
            <p><strong>Local Community Support:</strong> We invest in community-based conservation projects that provide alternative livelihoods to activities that damage the environment.</p>

