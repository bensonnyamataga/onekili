            <h2>Rainy Seasons: Challenges and Considerations</h2>
            <p>While the rainy seasons present additional challenges, they also offer unique advantages for experienced climbers seeking solitude and dramatic landscapes.</p>

            <h3>Long Rains: March to May (Most Challenging)</h3>
            <p>This period sees the heaviest rainfall and most difficult climbing conditions:</p>
            
            <p><strong>Weather Characteristics:</strong> Persistent rainfall, muddy trails, limited visibility, and colder overall conditions. Summit attempts often face snow and high winds.</p>
            
            <p><strong>Challenges:</strong> Wet gear, slippery trails, limited mountain views, and lower summit success rates (approximately 60-70%).</p>
            
            <p><strong>Potential Advantages:</strong> Complete solitude on the mountain, lush green landscapes, and lower prices. Only recommended for experienced climbers with proper wet weather gear.</p>

            <h3>Short Rains: November (Variable Conditions)</h3>
            <p>A transitional period with unpredictable weather patterns:</p>
            
            <p><strong>Weather Characteristics:</strong> Brief but intense afternoon showers, mixed sunny periods, and rapidly changing conditions. Mornings are typically clear with clouds building throughout the day.</p>
            
            <p><strong>Advantages:</strong> Fewer climbers, beautiful cloud formations, and dramatic lighting for photography.</p>
            
            <p><strong>Considerations:</strong> Unpredictable weather requires flexibility and comprehensive gear for both wet and dry conditions.</p>

            <div class="highlight-box secondary">
                <div class="highlight-icon">
                    <i class="fas fa-cloud-sun-rain"></i>
                </div>
                <p><strong>Local Guide Insight:</strong> The short rains in November are often called "green season" by local guides. While you might experience afternoon showers, the mornings are typically spectacularly clear, and the mountain is at its most beautiful with vibrant green vegetation and dramatic cloud formations.</p>
            </div>

            <h2>Temperature Ranges by Elevation</h2>
            <p>Understanding temperature variations is crucial for proper gear selection and mental preparation. Kilimanjaro's temperatures can swing dramatically between day and night and across different elevations.</p>

            <div class="image-with-caption">
                <img src="img/weather-for-kilimanjaro.jpg" alt="Kilimanjaro temperature ranges by elevation">
                <p>Temperature decreases approximately 1°C for every 200 meters gained in elevation</p>
            </div>

            <h3>Rainforest Zone (1,800-2,800m)</h3>
            <p>The warmest section of your climb with high humidity:</p>
            
            <p><strong>Daytime Temperatures:</strong> 20-25°C (68-77°F) - comfortable for hiking in light layers</p>
            
            <p><strong>Nighttime Temperatures:</strong> 10-15°C (50-59°F) - requiring light insulation</p>
            
            <p><strong>Special Considerations:</strong> High humidity can make temperatures feel warmer, and rain is common regardless of season in this zone.</p>

            <h3>Heath/Moorland Zone (2,800-4,000m)</h3>
            <p>Where temperatures begin to drop significantly:</p>
            
            <p><strong>Daytime Temperatures:</strong> 15-20°C (59-68°F) - perfect hiking weather with light layers</p>
            
            <p><strong>Nighttime Temperatures:</strong> 5-10°C (41-50°F) - requiring proper sleeping bags and insulation</p>
            
            <p><strong>Special Considerations:</strong> Strong sun exposure during the day contrasts with chilly evenings. Sun protection becomes increasingly important.</p>

            <h3>Alpine Desert and Summit Zones (4,000-5,895m)</h3>
            <p>The coldest sections where proper gear is essential:</p>
            
            <p><strong>Daytime Temperatures:</strong> 5-15°C (41-59°F) - but feels colder due to wind chill</p>
            
            <p><strong>Nighttime Temperatures:</strong> -5 to -15°C (23-5°F) - with summit nights reaching -20°C (-4°F) with wind chill</p>
            
            <p><strong>Special Considerations:</strong> Wind speeds can reach 50+ km/h, creating dangerous wind chill conditions. Proper summit gear is non-negotiable.</p>

