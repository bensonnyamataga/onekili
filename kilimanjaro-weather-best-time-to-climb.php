            <h2>Understanding Kilimanjaro's Climate Zones</h2>
            <p>Kilimanjaro's weather isn't just about seasons—it's about vertical climate zones that change dramatically as you ascend. Each zone presents unique weather conditions that require specific preparation and gear.</p>

            <div class="day-images">
                <img src="img/kilimanjaro-climbing.jpg" alt="Kilimanjaro sunrise weather conditions">
                <img src="img/best-time-Kilimanjaro.jpg" alt="Kilimanjaro seasonal weather variations">
            </div>
            
            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-mountain"></i>
                </div>
                <p>Kilimanjaro spans an incredible 4,900 meters (16,000 feet) of vertical relief, creating microclimates that range from equatorial to arctic conditions within just a few days of hiking.</p>
            </div>

            <p>The five distinct climate zones you'll experience:
            <ul>
                <li><strong>Cultivation Zone (800-1,800m):</strong> Warm temperatures, agricultural lands</li>
                <li><strong>Rainforest Zone (1,800-2,800m):</strong> Humid, frequent rainfall, lush vegetation</li>
                <li><strong>Heath/Moorland Zone (2,800-4,000m):</strong> Cooler temperatures, variable clouds</li>
                <li><strong>Alpine Desert Zone (4,000-5,000m):</strong> Extreme temperature swings, minimal vegetation</li>
                <li><strong>Arctic Summit Zone (5,000-5,895m):</strong> Freezing temperatures, high winds, snow conditions</li>
            </ul>
            </p>

            <h2>Seasonal Weather Patterns: When to Climb</h2>
            <p>Kilimanjaro experiences two distinct dry seasons and two rainy seasons, creating four primary climbing windows with different advantages and challenges.</p>

            <div class="image-with-caption">
                <img src="img/kilimanjaro-weather.jpg" alt="Kilimanjaro seasonal weather patterns chart">
                <p>Understanding seasonal patterns helps choose the optimal climbing window for your preferences and goals</p>
            </div>

            <h3>Long Dry Season: June to October (Best Conditions)</h3>
            <p>This period offers the most stable weather conditions and highest summit success rates:</p>
            
            <p><strong>Weather Characteristics:</strong> Clear skies, minimal precipitation, excellent visibility, and moderate temperatures at lower elevations. Summit nights are cold but generally clear.</p>
            
            <p><strong>Advantages:</strong> Dry trails, minimal rain gear needed, spectacular views, comfortable hiking temperatures during the day.</p>
            
            <p><strong>Considerations:</strong> This is the most popular time, meaning more climbers on the routes. Advance booking is essential, and temperatures can be quite cold at higher elevations, especially at night.</p>

            <h3>Short Dry Season: December to March (Excellent Alternative)</h3>
            <p>The second-best climbing window with generally good conditions:</p>
            
            <p><strong>Weather Characteristics:</strong> Warmer temperatures than the long dry season, occasional brief showers possible, generally clear skies with good visibility.</p>
            
            <p><strong>Advantages:</strong> Warmer conditions, fewer crowds than peak season, beautiful green landscapes from recent rains.</p>
            
            <p><strong>Considerations:</strong> Slightly higher chance of precipitation, particularly in March as the long rains approach. Summit conditions can be windy but generally manageable.</p>

