            <h3>Highlight Bird Species</h3>
            <p><strong>Hartlaub's Turaco:</strong> This stunning bird with crimson flight feathers is often heard before it's seen, with its distinctive "kok-kok-kok" call echoing through the rainforest.</p>
            
            <p><strong>Silvery-cheeked Hornbill:</strong> Large and noisy, these birds with their massive curved bills are frequently seen and heard in forested areas, particularly in fruiting trees.</p>
            
            <p><strong>Malachite Sunbird:</strong> As you ascend into the heath zone, watch for these iridescent green sunbirds feeding on protea and other high-altitude flowers.</p>
            
            <p><strong>White-necked Raven:</strong> These intelligent birds are commonly seen throughout the upper mountain zones, often following climbing parties in hopes of food scraps.</p>

