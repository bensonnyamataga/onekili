            <h2>Mammals of the Lower Slopes</h2>
            <p>Beyond primates, Kilimanjaro's forests and surrounding areas host an impressive array of mammals:</p>

            <h3>Large Mammals</h3>
            <p>While less commonly seen than primates, these magnificent animals inhabit Kilimanjaro's wilderness:</p>
            
            <p><strong>Elephants:</strong> Occasionally venture into the forest zone from surrounding areas, particularly on the mountain's western and southern slopes. These are forest elephants, smaller than their savanna relatives.</p>
            
            <p><strong>Buffalo:</strong> Cape buffalo may be encountered in forest clearings, especially near water sources. These powerful animals should be observed from a safe distance.</p>
            
            <p><strong>Bushbuck and Duiker:</strong> These smaller antelope species are more commonly seen, particularly at dawn and dusk when they venture to forest edges to feed.</p>

            <div class="highlight-box secondary">
                <div class="highlight-icon">
