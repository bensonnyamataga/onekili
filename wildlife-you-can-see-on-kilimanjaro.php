            <h2>Smaller Creatures and Insects</h2>
            <p>Beyond mammals and birds, Kilimanjaro hosts a fascinating array of smaller wildlife:</p>

            <h3>Reptiles and Amphibians</h3>
            <p>While not as numerous as in lower elevations, several reptile and amphibian species inhabit the mountain:</p>
            
            <p><strong>Montane Lizards:</strong> Several lizard species have adapted to Kilimanjaro's cooler temperatures, particularly in the forest and heath zones.</p>
            
            <p><strong>Tree Frogs:</strong> The rainforest provides ideal habitat for various frog species, whose calls add to the forest's soundscape during rainy periods.</p>
            
            <p><strong>Chameleons:</strong> Several chameleon species inhabit the forest understory, though their excellent camouflage makes them difficult to spot.</p>

