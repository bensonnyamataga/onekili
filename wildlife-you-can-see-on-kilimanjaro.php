            <p>Mount Kilimanjaro's unique position near the equator combined with its dramatic elevation changes creates five distinct ecological zones, each supporting specialized wildlife. While many climbers focus solely on reaching the summit, the journey offers incredible opportunities to observe some of Africa's most fascinating animals and birds in their natural habitats.</p>

            <h2>Kilimanjaro's Five Ecological Zones and Their Inhabitants</h2>
            <p>The mountain's vertical ecosystem creates what scientists call an "altitudinal gradient" - essentially multiple ecosystems stacked on top of each other. As you ascend, you'll pass through cultivated foothills, lush rainforest, heath and moorland, alpine desert, and finally the arctic summit zone.</p>
            
            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-tree"></i>
                </div>
                <p>Kilimanjaro National Park is home to 154 species of mammals, 7 species of primates, and over 180 bird species. The mountain's isolation has even led to the evolution of unique subspecies found nowhere else on Earth.</p>
            </div>

            <h2>Rainforest Zone (2,800m - 1,800m): The Wildlife Hotspot</h2>
            <p>The lush montane rainforest that encircles Kilimanjaro's lower slopes is where you'll encounter the most diverse and visible wildlife. This zone receives the most rainfall and supports dense vegetation that provides food and shelter for numerous species.</p>

            <div class="image-with-caption">
                <img src="img/rainforest-zone-kilimanjaro.jpg" alt="Kilimanjaro Rainforest Wildlife">
                <p>The rainforest zone offers the best wildlife viewing opportunities on Kilimanjaro</p>
