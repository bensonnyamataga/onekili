            <h3>Primates of the Rainforest</h3>
            <p>Kilimanjaro's forests are particularly rich in primate species:</p>
            
            <p><strong>Black and White Colobus Monkeys:</strong> These striking monkeys with their flowing white mantles and tails are one of Kilimanjaro's most iconic species. They live in troops of 5-15 individuals and are most active in early morning and late afternoon. Listen for their deep croaking calls echoing through the forest.</p>
            
            <p><strong>Blue Monkeys:</strong> Also known as diademed monkeys, these agile primates have grey bodies with reddish undersides and white throat patches. They're more numerous than colobus monkeys and often seen foraging in fruiting trees.</p>
            
            <p><strong>Olive Baboons:</strong> Larger than forest monkeys, baboons prefer the forest edges and more open areas. Troops can number up to 50 individuals and are fascinating to watch with their complex social behaviors.</p>

