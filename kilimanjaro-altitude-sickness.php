            <p>Altitude sickness, medically known as Acute Mountain Sickness (AMS), represents one of the most significant challenges facing climbers attempting Mount Kilimanjaro. As Africa's highest peak at 5,895 meters (19,341 feet), Kilimanjaro's summit sits in the "death zone" where oxygen levels are approximately 49% of those at sea level. Understanding how to prevent, recognize, and manage altitude-related illnesses is crucial not only for summit success but more importantly, for climber safety.</p>

            <h2>Understanding Altitude Sickness: The Science Behind AMS</h2>
            <p>Altitude sickness occurs when your body cannot get enough oxygen from the air at high elevations. As you ascend, atmospheric pressure decreases, reducing the amount of oxygen molecules in each breath. Your body must work harder to deliver oxygen to your tissues, leading to various physiological responses that can develop into AMS, High Altitude Pulmonary Edema (HAPE), or High Altitude Cerebral Edema (HACE).</p>
            
            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-lungs"></i>
                </div>
                <p>Research indicates that approximately 75% of climbers on Kilimanjaro will experience mild symptoms of altitude sickness, while 10-15% may develop more severe forms requiring medical intervention or descent.</p>
            </div>

            <p>The primary factors influencing susceptibility to altitude sickness include:
            <ul>
                <li>Rate of ascent - the single most important factor</li>
                <li>Individual physiological response to hypoxia</li>
                <li>Previous altitude experience</li>
                <li>Genetic predisposition</li>
                <li>Current health status and hydration levels</li>
            </ul>
            </p>

            <h2>Recognizing the Symptoms: From Mild to Severe</h2>
            <p>Early recognition of altitude sickness symptoms is crucial for proper management. Symptoms typically appear 6-12 hours after ascent and can range from mild discomfort to life-threatening conditions.</p>

            <div class="image-with-caption">
                <img src="img/altitude-sickness-disappoitment.jpg" alt="Altitude sickness symptom progression chart">
                <p>Understanding the progression of altitude sickness symptoms helps in early detection and intervention</p>
            </div>

            <h3>Mild to Moderate AMS Symptoms</h3>
            <p>These symptoms are common and usually manageable with proper care:</p>
            
            <p><strong>Headache:</strong> The most common symptom, often described as throbbing and worsening at night or with exertion. Unlike regular headaches, altitude headaches typically don't respond well to over-the-counter pain medication.</p>
            
            <p><strong>Nausea and Loss of Appetite:</strong> Many climbers experience digestive disturbances, including nausea, vomiting, and complete loss of appetite, even when physically exhausted and in need of calories.</p>
            
            <p><strong>Fatigue and Weakness:</strong> Unusual tiredness that isn't proportional to physical exertion, combined with general lethargy and difficulty performing normal climbing activities.</p>
            
            <p><strong>Dizziness and Lightheadedness:</strong> Feeling unsteady or dizzy, especially when standing up quickly or changing positions rapidly.</p>

            <h3>Severe AMS and Life-Threatening Conditions</h3>
            <p>These symptoms indicate progression to severe AMS, HAPE, or HACE and require immediate descent:</p>
            
            <p><strong>Severe Headache:</strong> Unrelenting headache that doesn't respond to medication, often described as the worst headache of one's life.</p>
            
            <p><strong>Ataxia:</strong> Loss of coordination and balance, difficulty walking heel-to-toe in a straight line, stumbling, or dropping objects.</p>
            
            <p><strong>Mental Changes:</strong> Confusion, disorientation, hallucinations, or changes in behavior that are noticeable to others in the climbing party.</p>
            
            <p><strong>Respiratory Symptoms:</strong> Persistent dry cough, shortness of breath at rest, gurgling or rattling sounds in the chest, or coughing up pink, frothy sputum.</p>

