            <h2>Emergency Protocols: When to Descend</h2>
            <p>Recognizing when symptoms require immediate descent is the most critical decision in altitude sickness management.</p>

            <h3>The Lake Louise Score System</h3>
            <p>This internationally recognized tool helps objectively assess AMS severity:</p>
            
            <p><strong>Mild AMS (Score 3-5):</strong> Can usually continue ascent with careful monitoring and symptom management.</p>
            
            <p><strong>Moderate AMS (Score 6-9):</strong> Should not ascend further and may require descent if symptoms don't improve with rest.</p>
            
            <p><strong>Severe AMS (Score 10+):</strong> Requires immediate descent of at least 500 meters (1,640 feet) and medical evaluation.</p>

            <h3>Descent Decision Framework</h3>
            <p>Our guides follow strict protocols for determining when descent is necessary:</p>
            
            <p><strong>Absolute Descent Indicators:</strong> Symptoms of HACE (ataxia, mental status changes) or HAPE (respiratory distress at rest, cough with frothy sputum) require immediate descent regardless of other factors.</p>
            
            <p><strong>Relative Descent Indicators:</strong> Worsening symptoms despite rest and medication, inability to keep down fluids, or symptoms severe enough to prevent normal activity.</p>
            
            <p><strong>Precautionary Descent:</strong> For climbers with significant risk factors or concerning symptom progression, even without severe symptoms.</p>

            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-exclamation-triangle"></i>
                </div>
                <p>Descent is the only definitive treatment for severe altitude sickness. Portable hyperbaric chambers (Gamow bags) can provide temporary relief but should never delay descent in true emergencies. Our guides are trained to recognize when immediate evacuation is necessary.</p>
            </div>

            <h2>Risk Factors and Pre-Existing Conditions</h2>
            <p>Understanding personal risk factors helps in preparing adequately and making informed decisions about Kilimanjaro climbing.</p>

            <h3>Medical Conditions Requiring Special Consideration</h3>
            <p>Certain conditions require medical clearance and special preparations:</p>
            
            <p><strong>Cardiovascular Conditions:</strong> Individuals with heart conditions require thorough medical evaluation, as altitude places additional strain on the cardiovascular system.</p>
            
            <p><strong>Respiratory Conditions:</strong> Asthma, COPD, or other lung diseases may be exacerbated by altitude, cold air, and physical exertion.</p>
            
            <p><strong>Pregnancy:</strong> Most operators, including ours, do not allow pregnant women to attempt Kilimanjaro due to unknown effects on fetal development at high altitude.</p>
            
            <p><strong>Recent Surgeries:</strong> Abdominal, chest, or brain surgeries within the previous year typically contraindicate high-altitude exposure.</p>

            <h3>Modifiable Risk Factors</h3>
            <p>Factors you can control to reduce your altitude sickness risk:</p>
            
            <p><strong>Physical Fitness:</strong> While fitness doesn't prevent AMS, it reduces overall physiological stress, making acclimatization more effective.</p>
            
            <p><strong>Sleep Quality:</strong> Prioritize good sleep habits before and during your climb, as sleep deprivation worsens AMS susceptibility.</p>
            
            <p><strong>Mental Preparation:</strong> Understanding altitude sickness reduces anxiety and helps climbers make rational decisions when symptoms occur.</p>

