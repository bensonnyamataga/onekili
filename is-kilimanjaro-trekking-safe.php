            </div>

            <h2>The Real Risks: Understanding What Can Go Wrong</h2>
            <p>To properly assess safety, we must honestly examine the potential risks. The primary concerns on Kilimanjaro fall into several categories, each with its own prevention and management strategies. Understanding these risks is the first step in mitigating them effectively.</p>

            <div class="image-with-caption">
