            <h2>Route Selection: How Your Itinerary Impacts Safety</h2>
            <p>Not all Kilimanjaro routes are created equal when it comes to safety. The route you choose significantly impacts your acclimatization profile, which in turn affects your altitude sickness risk. Understanding the safety implications of different routes is crucial for making an informed decision.</p>

            <div class="image-with-caption">
                <img src="img/route-safety.jpg" alt="Comparison of Kilimanjaro route safety profiles">
                <p>Routes with better acclimatization profiles significantly reduce altitude sickness risks</p>
